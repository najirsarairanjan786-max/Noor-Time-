export type LanguageCode = keyof typeof translations;

export function useTranslation(language: string) {
  let langKey = language as LanguageCode;
  if (!translations[langKey]) {
    if (language === 'en') langKey = 'English';
    else if (language === 'ur') langKey = 'Urdu';
    else langKey = 'English';
  }
  
  const t = (key: keyof typeof translations['English']) => {
    return translations[langKey]?.[key] || translations['English'][key];
  };

  const isRTL = langKey === 'Arabic' || langKey === 'Urdu';

  return { t, isRTL, langKey };
}
